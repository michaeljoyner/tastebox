        <p class="my-6">TasteBox began as a mother-daughter team that set out to teach people that cooking healthy food at home is actually easy, and they’re doing it one delicious, nutritious meal at a time.</p>

        <p class="my-6">From the TasteBox HQ in Hilton, each week the team tests and prepares a number of well-balanced, down-to-earth meals that are easy to cook and suited to the South African palette. The ingredients are ethically sourced, chopped, weighed, packed, and delivered with care every week so singles, couples, and families can learn to cook healthy meals at home.</p>

        <p class="my-6">Steph is a fully qualified dietician with 10 years of experience, 9 Comrades finishes, and mother to a beautiful baby boy. A passionate cook and long-time champion of health, Steph oversees the planning of each meal to ensure an equal measure of nutrition, convenience, and taste gets delivered in each meal kit.</p>

